package com.aca.Gift_It.model;

public class Party {

	String partyId;
	String partyLocation;
	String  partyDate;
	int partyDays;
	public int getPartyDays() {
		return partyDays;
	}
	public void setPartyDays(int partyDays) {
		this.partyDays = partyDays;
	}
	public String getPartyId() {
		return partyId;
	}
	public void setPartyId(String partyId) {
		this.partyId = partyId;
	}
	public String getPartyLocation() {
		return partyLocation;
	}
	public void setPartyLocation(String partyLocation) {
		this.partyLocation = partyLocation;
	}
	public String getPartyDate() {
		return partyDate;
	}
	public void setPartyDate(String partyDate) {
		this.partyDate = partyDate;
	}

	
}
